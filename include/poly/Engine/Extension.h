#ifndef POLY_EXTENSION_H
#define POLY_EXTENSION_H

namespace poly
{

class Scene;


///////////////////////////////////////////////////////////
/// \brief The base class for scene extensions
///
///////////////////////////////////////////////////////////
class Extension
{
public:
	///////////////////////////////////////////////////////////
	/// \brief A scene pointer is required for scene extensions
	///
	/// \param scene A scene pointer
	///
	///////////////////////////////////////////////////////////
	Extension(Scene* scene);

	///////////////////////////////////////////////////////////
	/// \brief Destructor
	///
	///////////////////////////////////////////////////////////
	virtual ~Extension();

protected:
	Scene* m_scene;			//!< A pointer to a scene
};

}

#endif