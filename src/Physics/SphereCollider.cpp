#include <poly/Physics/SphereCollider.h>

namespace poly
{


///////////////////////////////////////////////////////////
SphereCollider::SphereCollider() :
	m_radius		(0.5f)
{

}


///////////////////////////////////////////////////////////
SphereCollider::SphereCollider(float radius) :
	m_radius		(radius)
{

}


///////////////////////////////////////////////////////////
void SphereCollider::setRadius(float radius)
{
	m_radius = radius;
}


///////////////////////////////////////////////////////////
float SphereCollider::getRadius() const
{
	return m_radius;
}


}