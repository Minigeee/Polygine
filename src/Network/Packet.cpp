#include <poly/Network/Packet.h>

namespace poly
{


///////////////////////////////////////////////////////////
Packet::Packet()
{

}


///////////////////////////////////////////////////////////
Packet::operator bool() const
{
	return (bool)m_packet;
}


///////////////////////////////////////////////////////////
void Packet::write(const void* data, Uint32 size)
{
	return m_packet.append(data, size);
}


///////////////////////////////////////////////////////////
void Packet::clear()
{
	m_packet.clear();
}


///////////////////////////////////////////////////////////
void* Packet::getData(Uint32 offset) const
{
	return (Uint8*)m_packet.getData() + offset;
}


///////////////////////////////////////////////////////////
Uint32 Packet::getSize() const
{
	return m_packet.getDataSize();
}


///////////////////////////////////////////////////////////
Uint32 Packet::getReadOffset() const
{
	return m_packet.getReadPosition();
}


///////////////////////////////////////////////////////////
bool Packet::endOfPacket() const
{
	return m_packet.endOfPacket();
}


}